from fpdf import FPDF

pdf = FPDF('P', 'mm', 'A4')
pdf.add_page()
pdf.set_margins(0, 0, 0)
pdf.set_font('Arial', 'B', 14)
pdf.cell(40, 10, 'Hello World!', border = 0, ln = 1, align = '', fill = False, link = '')
pdf.set_font('Times', '', 12)
pdf.cell(210, 10, 'Powered by FPDF.', 0, 0, 'C')
pdf.set_font('Arial', '', 12)
pdf.ln(50)
pdf.cell(85)
pdf.cell(0, 0, 'We have a new line.', 0)
pdf.image('img.jpg', x = 85, y = 32.5, w = 40, h = 0, type = '', link = '')
pdf.line(x1 = 85, y1 = 27.5, x2 = 125, y2 = 27.5)
pdf.rect(x = 80, y = 20, w = 50, h = 55, style = '')
pdf.set_fill_color(128, 166, 197)
pdf.set_draw_color(41, 53, 50)
pdf.rect(80, 115, 50, 10, 'DF')

pdf.set_text_color(183, 208, 232)
pdf.ln(50)
pdf.cell(70)
pdf.cell(70, 0, 'We have a new line.', 0, 0, 'C')
for i in range(17):
    pdf.image('img.jpg', 20 + i * 10, 102.5, 5)
    pdf.image('img.jpg', 20 + i * 10, 82.5, 5)
    pdf.image('img.jpg', 25 + i * 10, 102.5, 5)
    pdf.image('img.jpg', 25 + i * 10, 82.5, 5)
pdf.output('sample.pdf', 'F')